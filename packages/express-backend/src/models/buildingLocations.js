import mongoose from "mongoose";

const buildingLocationSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  number: {
    type: String,
    required: true,
    trim: true,
  },
  latitude: {
    type: Number,
    required: true,
  },
  longitude: {
    type: Number,
    required: true,
  },
});

const BuildingLocation = mongoose.model(
  "BuildingLocation",
  buildingLocationSchema,
);

export default BuildingLocation;
